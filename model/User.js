const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  gender: {
    type: String,
    required: true
  },
  phoneNo: {
    type: Number
  },
  language: {
    type: String
  },
});

module.exports = mongoose.model('user', UserSchema);